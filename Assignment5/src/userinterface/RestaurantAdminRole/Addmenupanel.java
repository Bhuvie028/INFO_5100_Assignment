    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
        int selectedRow = menutable.getSelectedRow();
        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(null,"Please Select a row from table first", "Warining", JOptionPane.WARNING_MESSAGE);
            return;
        }
        m = (Menu)menutable.getValueAt(selectedRow,0);
        txtmmt.setText(m.getItemName());
        txtmiq.setText(String.valueOf(m.getQuantity()));
        txtmip.setText(String.valueOf(m.getItemPrice()));
        }
        catch(Exception E){
            JOptionPane.showMessageDialog(null,"Please provide appropriate value to proceed");  
        }
    }//GEN-LAST:event_jButton1ActionPerformed

