    private void btaddmenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btaddmenuActionPerformed
        try{
        String menuIt = menuitemtxt.getText();
        Double ip = Double.valueOf(itempricetxt.getText());
        Integer quantity= Integer.valueOf(itemquantitytxt.getText());
        Menu m = new Menu();
        m.setItemName(menuIt);
        m.setItemPrice(ip);
        m.setQuantity(quantity);
        Restaurant ro = system.getRestaurantDirectory().findRestaurant(restaurantName);
        if(ro.getMd() == null){
            MenuDirectory md = new MenuDirectory();
            ArrayList<Menu> am  = new ArrayList<>();
            am.add(m);
            md.setMenu(am);
            ro.setMd(md);  
        }
        else{
            MenuDirectory md =   ro.getMd();
            ArrayList<Menu> am  = ro.getMd().getMenu();
            am.add(m);
            md.setMenu(am);
            ro.setMd(md);
        }
        populatetable();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,"Please provide appropriate value to proceed"); 
        }
    }//GEN-LAST:event_btaddmenuActionPerformed

