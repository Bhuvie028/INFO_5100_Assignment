    public void populatetable(){
        DefaultTableModel defaultmenutble = (DefaultTableModel) menutable.getModel();
        defaultmenutble.setRowCount(0);
        Restaurant ro1 = system.getRestaurantDirectory().findRestaurant(restaurantName);
        if(ro1.getMd()!=null){
            for(Menu m : ro1.getMd().getMenu()) {
                Object row[] = new Object[3];
                row[0] = m;
                row[1] = m.getItemPrice();
                row[2] = m.getQuantity();
                defaultmenutble.addRow(row);
            }
        }
    }
