    public String findRestaurantName(String name){
        for(Restaurant restaurant : system.getRestaurantDirectory().getRestaurantList()){
            if(name.equals(restaurant.getManager())){
                return restaurant.getName();
            }
        }
        return "NOT FOUND"; 
    }
    
